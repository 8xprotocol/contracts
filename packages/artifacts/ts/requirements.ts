// tslint:enable:no-unused-variable

export type RequirementsEventArgs =
    | RequirementsOwnershipTransferredEventArgs;

export enum RequirementsEvents {
    OwnershipTransferred = 'OwnershipTransferred',
}

export interface RequirementsOwnershipTransferredEventArgs extends DecodedLogArgs {
    previousOwner: string;
    newOwner: string;
}


/* istanbul ignore next */
